// Helper functions
export const getRandomWords = (count: number = 10): Word[] => {
  const allWords = topics.flatMap(topic => topic.words);
  const shuffled = [...allWords].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getTopicById = (topicId: string): Topic | undefined => {
  return topics.find(topic => topic.id === topicId);
};

export const getWordsByTopicId = (topicId: string): Word[] => {
  const topic = getTopicById(topicId);
  return topic?.words || [];
};
